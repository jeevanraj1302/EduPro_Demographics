from email.mime.text import MIMEText
from email.mime.base import MIMEBase
from email import encoders
from datetime import datetime
from typing import Optional

from loguru import logger

from src.config import (
    SMTP_HOST, SMTP_PORT, SMTP_USER, SMTP_PASSWORD,
    EMAIL_FROM, EMAIL_DEMO_MODE, EMAILS_DIR,
)


# ══════════════════════════════════════════════
# Email Composition
# ══════════════════════════════════════════════
def compose_report_email(
    recipients: list[str],
    subject: str,
    body_text: str,
    report_attachment: Optional[bytes] = None,
    attachment_name: str = "edupro_report.txt",
) -> MIMEMultipart:
    """
    Compose an email with optional report attachment.

    Parameters
    ----------
    recipients : list[str]
        Email recipient addresses.
    subject : str
        Email subject line.
    body_text : str
        Email body content.
    report_attachment : bytes, optional
        Report file content to attach.
    attachment_name : str
        Filename for the attachment.

    Returns
    -------
    MIMEMultipart
        Composed email message.
    """
    msg = MIMEMultipart()
    msg["From"] = EMAIL_FROM
    msg["To"] = ", ".join(recipients)
    msg["Subject"] = subject
    msg["Date"] = datetime.now().strftime("%a, %d %b %Y %H:%M:%S +0000")

    # HTML body
    html_body = f"""
    <html>
    <head>
        <style>
            body {{ font-family: 'Segoe UI', Arial, sans-serif; color: #333; }}
            .header {{ background: linear-gradient(135deg, #6C63FF, #FF6584);
                       padding: 20px; text-align: center; border-radius: 8px; }}
            .header h1 {{ color: white; margin: 0; font-size: 24px; }}
            .content {{ padding: 20px; background: #f9f9f9; border-radius: 8px;
                        margin-top: 15px; }}
            .footer {{ text-align: center; padding: 15px; color: #999; font-size: 12px; }}
        </style>
    </head>
    <body>
        <div class="header">
            <h1>🎓 EduPro Demographics Report</h1>
        </div>
        <div class="content">
            {body_text.replace(chr(10), '<br>')}
        </div>
        <div class="footer">
            <p>This report was auto-generated by EduPro Analytics on
               {datetime.now().strftime('%B %d, %Y at %I:%M %p')}.</p>
            <p>© 2026 EduPro Analytics Team</p>
        </div>
    </body>
    </html>
    """
    msg.attach(MIMEText(html_body, "html"))

    # Attachment
    if report_attachment:
        part = MIMEBase("application", "octet-stream")
        part.set_payload(report_attachment)
        encoders.encode_base64(part)
        part.add_header(
            "Content-Disposition",
            f"attachment; filename={attachment_name}",
        )
        msg.attach(part)

    return msg


# ══════════════════════════════════════════════
# Email Sending
# ══════════════════════════════════════════════
def send_email(msg: MIMEMultipart) -> tuple[bool, str]:
    """
    Send an email via SMTP or save to disk in demo mode.

    Returns
    -------
    tuple[bool, str]
        (success, message)
    """
    if EMAIL_DEMO_MODE:
        return _save_demo_email(msg)
    return _send_smtp_email(msg)


def _send_smtp_email(msg: MIMEMultipart) -> tuple[bool, str]:
    """Send email via configured SMTP server."""
    if not SMTP_USER or not SMTP_PASSWORD:
        return False, "SMTP credentials not configured. Set SMTP_USER and SMTP_PASSWORD environment variables."

    try:
        with smtplib.SMTP(SMTP_HOST, SMTP_PORT) as server:
            server.starttls()
            server.login(SMTP_USER, SMTP_PASSWORD)
            server.send_message(msg)
        logger.success(f"Email sent to: {msg['To']}")
        return True, f"Email sent successfully to {msg['To']}"
    except Exception as exc:
        logger.error(f"Failed to send email: {exc}")
        return False, f"Failed to send email: {exc}"


def _save_demo_email(msg: MIMEMultipart) -> tuple[bool, str]:
    """Save email to disk in demo mode instead of sending."""
    try:
        EMAILS_DIR.mkdir(parents=True, exist_ok=True)
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        filename = f"report_email_{timestamp}.eml"
        filepath = EMAILS_DIR / filename
        filepath.write_text(msg.as_string(), encoding="utf-8")
        logger.success(f"Demo email saved to: {filepath}")
        return True, f"Demo mode: Email saved to {filepath}"
    except Exception as exc:
        logger.error(f"Failed to save demo email: {exc}")
        return False, f"Failed to save demo email: {exc}"


