def create_daily_summary_email(
    recipients: list[str],
    kpis: dict,
    report_text: Optional[str] = None,
    pdf_bytes: Optional[bytes] = None,
) -> MIMEMultipart:
    """Create a daily summary report email."""
    subject = f"📊 EduPro Daily Report — {datetime.now().strftime('%B %d, %Y')}"

    body = f"""
    <h2>📊 Daily Analytics Summary</h2>

    <table style="border-collapse: collapse; width: 100%;">
        <tr style="background: #6C63FF; color: white;">
            <th style="padding: 10px; text-align: left;">Metric</th>
            <th style="padding: 10px; text-align: right;">Value</th>
        </tr>
        <tr><td style="padding: 8px; border-bottom: 1px solid #eee;">Total Learners</td>
            <td style="padding: 8px; text-align: right; border-bottom: 1px solid #eee;">{kpis.get('total_learners', 'N/A'):,}</td></tr>
        <tr><td style="padding: 8px; border-bottom: 1px solid #eee;">Average Age</td>
            <td style="padding: 8px; text-align: right; border-bottom: 1px solid #eee;">{kpis.get('average_age', 'N/A')}</td></tr>
        <tr><td style="padding: 8px; border-bottom: 1px solid #eee;">Male / Female</td>
            <td style="padding: 8px; text-align: right; border-bottom: 1px solid #eee;">{kpis.get('male_count', 0):,} / {kpis.get('female_count', 0):,}</td></tr>
        <tr><td style="padding: 8px; border-bottom: 1px solid #eee;">Gender Ratio</td>
            <td style="padding: 8px; text-align: right; border-bottom: 1px solid #eee;">{kpis.get('gender_ratio', 'N/A')}</td></tr>
        <tr><td style="padding: 8px;">Top Email Provider</td>
            <td style="padding: 8px; text-align: right;">{kpis.get('most_common_provider', 'N/A')}</td></tr>
    </table>
    """

    attachment = None
    attachment_name = "edupro_report.txt"

    if pdf_bytes:
        attachment = pdf_bytes
        attachment_name = f"edupro_report_{datetime.now().strftime('%Y%m%d')}.pdf"
    elif report_text:
        attachment = report_text.encode("utf-8")
        attachment_name = f"edupro_report_{datetime.now().strftime('%Y%m%d')}.txt"

    return compose_report_email(
        recipients=recipients,
        subject=subject,
        body_text=body,
        report_attachment=attachment,
        attachment_name=attachment_name,
    )


